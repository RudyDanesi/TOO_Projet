    private void displayButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_displayButtonMouseClicked
        // TODO add your handling code here:
        ArrayList<Document> dl = new ArrayList(maMediatheque.getDocumentList());
        int index = jdocumentList.getSelectedIndex();
        Object o = jdocumentList.getModel().getElementAt(index);
        String document[] = o.toString().split(" \\(");
        myConsole.setText(null);

        int i=0;
        while (i < dl.size())
        {
             if (dl.get(i).getTitreDoc().compareTo(document[0]) == 0)
             {
                 dl.get(i).displayDocument(myConsole);
             }
             i++;
        }
        //maMediatheque.getDocumentList().get(index).displayDocument(myConsole);
    }//GEN-LAST:event_displayButtonMouseClicked

    
