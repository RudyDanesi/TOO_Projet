    private void creationButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_creationButtonMouseClicked
        // TODO add your handling code here:
        maMediatheque = new Mediatheque(this,myConsole);
        creationButton.setEnabled(false);
        displayButton.setEnabled(true);
        resetButton.setEnabled(true);
        sortButton.setEnabled(true);
        searchButton.setEnabled(true);
        addButton.setEnabled(true);
        creationButton.setToolTipText("<html>Button disabled <br/> Library already created</html>");
        
        ArrayList<Document> myDocumentList = new ArrayList(maMediatheque.getDocumentList());    
        listDefault = new DefaultListModel();
        for (int i=0; i < myDocumentList.size(); i++)
        {
            listDefault.addElement(myDocumentList.get(i).getTitreDoc()
                                        +" ("
                                        +myDocumentList.get(i).getNomAuteur()
                                        +")");
        }
        jdocumentList.setModel(listDefault);
    }//GEN-LAST:event_creationButtonMouseClicked

    private void okButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_okButtonMouseClicked
        // TODO add your handling code here:
        if (authorButton.isSelected())
        {
            maMediatheque.triParAuteur();
        }
        else if (titleButton.isSelected())
        {
            maMediatheque.triParTitre();
        }
        else
        {
            // Alert Message
            JOptionPane.showMessageDialog
                    (this,
                    "Please choose an option before clicking the OK button",
                    "Information",
                    JOptionPane.WARNING_MESSAGE);
        }
        
        ArrayList<Document> myDocumentList = new ArrayList(maMediatheque.getDocumentList());    
        DefaultListModel buffer = new DefaultListModel();
        for (int i=0; i < myDocumentList.size(); i++)
        {
            buffer.addElement(myDocumentList.get(i).getTitreDoc()
                                        +" ("
                                        +myDocumentList.get(i).getNomAuteur()
                                        +")");
        }
        jdocumentList.setModel(buffer);
    }//GEN-LAST:event_okButtonMouseClicked

    private void addButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addButtonMouseClicked
        // TODO add your handling code here:
        maMediatheque.addDocument();
        ArrayList<Document> myDocumentList = new ArrayList(maMediatheque.getDocumentList());
        int lastElement = ( myDocumentList.size() - 1 );
        listDefault.addElement(myDocumentList.get(lastElement).getTitreDoc()
                                        +" ("
                                        +myDocumentList.get(lastElement).getNomAuteur()
                                        +")");
        jdocumentList.setModel(listDefault);
    }//GEN-LAST:event_addButtonMouseClicked

    private void okButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_okButton2MouseClicked
        // TODO add your handling code here:
        if (authorButton2.isSelected())
        {
            maMediatheque.searchByAuthor(searchText.getText());
            ArrayList<Document> myDocumentList = new ArrayList(maMediatheque.getSearchList());    
            DefaultListModel buffer = new DefaultListModel();
            for (int i=0; i < myDocumentList.size(); i++)
            {
                buffer.addElement(myDocumentList.get(i).getTitreDoc()
                                        +" ("
                                        +myDocumentList.get(i).getNomAuteur()
                                        +")");
            }
            jdocumentList.setModel(buffer);
        }
        else if (titleButton2.isSelected())
        {
            JOptionPane.showMessageDialog(this,
                            "This function is not implemented yet");
        }
        else
        {
            // Alert Message
            JOptionPane.showMessageDialog
                    (this,
                    "Please choose an option before clicking the OK button",
                    "Information",
                    JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_okButton2MouseClicked

